public class Aula04OperadoresAtribuitivos {
    /* =; +=; -=; *=; /=; %=*/

    //+=
    public static void main(String[] args) {
        double bonus = 2750;
        bonus += 356; //3106
        bonus -= 254; //2852
        bonus *= 3; //8556
        bonus /= 6; //1426
        bonus %= 6; //4

        System.out.println (bonus);
    }
}

